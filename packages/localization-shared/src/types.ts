// ============================================================================

/**
 * Request to generate localization (text or audio)
 */
export interface LocalizationRequest {
  /** POI identifier */
  poiId: string;
  /** Target language for localization */
  targetLanguage: BCP47Language;
  /** Type of localization requested */
  type: 'text' | 'audio' | 'both';
  /** Optional context for generation */
  context?: {
    sourceLanguage?: BCP47Language;
    tone?: string;
    maxLength?: number;
  };
}

/**
 * Response from localization request
 */
export interface LocalizationRequestResponse {
  /** Unique request identifier */
  requestId: string;
  /** Current status of the request */
  status: 'pending' | 'processing' | 'completed' | 'failed';
  /** Optional error message */
  error?: string;
  /** Optional generated localization data */
  data?: PoiLocalization;
}


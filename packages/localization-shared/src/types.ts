/**
 * Check if a value is a valid BCP47Language
 */
export const isBCP47Language = (value: unknown): value is BCP47Language => {
  if (typeof value !== 'string') return false;
  // Simple BCP 47 validation: language tag format
  return /^[a-z]{2,3}(-[a-z]{2})?(-[a-z]{4})?(-[a-z]{2})?(-[x-][a-z0-9]+)*$/i.test(
    value
  );
};

/**
 * Check if a value is a SupportedLanguage
 */
export const isSupportedLanguage = (
  value: unknown
): value is SupportedLanguage => {
  if (typeof value !== 'object' || value === null) return false;
  const obj = value as Record<string, unknown>;
  return (
    typeof obj.code === 'string' &&
    typeof obj.label === 'string' &&
    typeof obj.enabled === 'boolean' &&
    typeof obj.supportsText === 'boolean' &&
    typeof obj.supportsTts === 'boolean' &&
    typeof obj.priority === 'number' &&
    typeof obj.shopOwnerEditable === 'boolean' &&
    typeof obj.updatedAt === 'string'
  );
};

/**
 * Check if a value is a PoiLocalization
 */
export const isPoiLocalization = (
  value: unknown
): value is PoiLocalization => {
  if (typeof value !== 'object' || value === null) return false;
  const obj = value as Record<string, unknown>;
  return (
    typeof obj.language === 'string' &&
    typeof obj.name === 'string' &&
    typeof obj.description === 'string' &&
    typeof obj.autoGenerated === 'boolean' &&
    typeof obj.updatedAt === 'string' &&
    typeof obj.updatedBy === 'string'
  );
};
